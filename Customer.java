public class Customer {
    private int cust_id;
    private String cust_name, cust_email, cust_phoneNo;

    /**
     * @return the cust_id
     */
    public int getCust_id() {
        return cust_id;
    }

    /**
     * @param cust_id the cust_id to set
     */
    public void setCust_id(int cust_id) {
        this.cust_id = cust_id;
    }

    /**
     * @return the cust_name
     */
    public String getCust_name() {
        return cust_name;
    }

    /**
     * @param cust_name the cust_name to set
     */
    public void setCust_name(String cust_name) {
        this.cust_name = cust_name;
    }

    /**
     * @return the cust_email
     */
    public String getCust_email() {
        return cust_email;
    }

    /**
     * @param cust_email the cust_email to set
     */
    public void setCust_email(String cust_email) {
        this.cust_email = cust_email;
    }

    /**
     * @return the cust_phoneNo
     */
    public String getCust_phoneNo() {
        return cust_phoneNo;
    }

    /**
     * @param cust_phoneNo the cust_phoneNo to set
     */
    public void setCust_phoneNo(String cust_phoneNo) {
        this.cust_phoneNo = cust_phoneNo;
    } 
}
