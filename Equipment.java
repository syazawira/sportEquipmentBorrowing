package Sport.com;

/**
 *
 * @author LENOVO
 */
public class Equipment {
    private int equip_id;
    private String equip_name, category;
    private int available_quantity;

    /**
     * @return the equip_id
     */
    public int getEquip_id() {
        return equip_id;
    }

    /**
     * @param equip_id the equip_id to set
     */
    public void setEquip_id(int equip_id) {
        this.equip_id = equip_id;
    }

    /**
     * @return the equip_name
     */
    public String getEquip_name() {
        return equip_name;
    }

    /**
     * @param equip_name the equip_name to set
     */
    public void setEquip_name(String equip_name) {
        this.equip_name = equip_name;
    }

    /**
     * @return the category
     */
    public String getCategory() {
        return category;
    }

    /**
     * @param category the category to set
     */
    public void setCategory(String category) {
        this.category = category;
    }

    /**
     * @return the available_quantity
     */
    public int getAvailable_quantity() {
        return available_quantity;
    }

    /**
     * @param available_quantity the available_quantity to set
     */
    public void setAvailable_quantity(int available_quantity) {
        this.available_quantity = available_quantity;
    }
    
    
    
}
